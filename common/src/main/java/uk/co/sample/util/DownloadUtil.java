    //***** public method *****
    public static void setAttachment(ServletContext context, HttpServletResponse response, String fileName) {
        String mimeType = context.getMimeType(fileName);
        if (mimeType != null) {
            response.setContentType(mimeType);
        } else {
            String extension = FilenameUtils.getExtension(fileName);
            if (MIME_MAPPING.containsKey(extension)) {
                response.setContentType(MIME_MAPPING.get(extension));
            } else {
                response.setContentType("application/octet-stream");
            }
        }
        response.setHeader("Content-Disposition", "attachment; filename=\"" + doEncode(doFilterFileName(fileName)) + "\"");
    }

    private static String doFilterFileName(String name) {
        if (StringUtils.isEmpty(name)) {
            return name;
        }
        StringBuilder builder = new StringBuilder(name.length());
        for (int i = 0; i < name.length(); i++) {
            char c = name.charAt(i);
            switch (c) {
            case ':':
            case '/':
            case '[':
            case ']':
            case '<':
            case '>':
            case '|':
            case '\\':
            case '\r':
            case '\n':
            case '\t':
                builder.append("_");
                break;
            default:
                builder.append(c);
                break;
            }
        }
        return builder.toString();
    }

    private static String doEncode(String string) {
        try {
            return new String(string.getBytes("Windows-31J"), StandardCharsets.ISO_8859_1);
        } catch (UnsupportedEncodingException e) {
            throw new ApplicationLogicException(ERROR_LOGIC, e);
        }
    }

